import { writeFileSync } from 'fs';
import elements from '@vonage/vvd-umbrella/custom-elements.json';

console.log(
  '\x1b[33m%s\x1b[0m',
  '** generating css selectors from custom-elements.json **'
);

const { tags } = elements;
const cssRule = `${ tags.map(({name})=> name).join(',') } { visibility: hidden; }`;

const filePath = 'src/pre-scheme-loading.autogenerated.scss';
writeFileSync(filePath, cssRule);
